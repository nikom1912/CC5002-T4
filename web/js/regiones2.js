function validarRyC(){
    region = document.getElementById("regiones").value;
    comuna = document.getElementById("comunas").value;
    region2 = document.getElementById("regiones2").value;
    comuna2 = document.getElementById("comunas2").value;

    validatorR1 = false;
    validatorR2 = false;
    validatorC1 = false;
    validatorC2 = false;
    for(i = 0; i< RegionesYcomunas.regiones.length; i++ ){
      if(RegionesYcomunas.regiones[i].NombreRegion == region){
          validatorR1 = true;
          for(j=0; j < RegionesYcomunas.regiones[i].comunas.length; j++){
              if(RegionesYcomunas.regiones[i].comunas[j] == comuna){
                  validatorC1 = true;
              }
          }
      }
      if(RegionesYcomunas.regiones[i].NombreRegion == region2){
          validatorR2 = true;
          for(j=0; j < RegionesYcomunas.regiones[i].comunas.length; j++){
            if(RegionesYcomunas.regiones[i].comunas[j] == comuna2){
                validatorC2 = true;
            }
        }
      }
    }
    
    if(validatorR1 == false){
        alert(region + ': Region de origen invalida' );
        return false;
    }
    if(validatorC1 == false){
        alert(comuna + ': No coincide con region de origen ' + region);
        return false;
    }
    if(validatorR2 == false){
        alert(region2 + ': Region de destino invalida');
        return false;
    }
    if(validatorC2 == false){
        alert(comuna2 + ': No coincide con region de destino ' + region2);
        return false;
    }
    
    return true;
    };

